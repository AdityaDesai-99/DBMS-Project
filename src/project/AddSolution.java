    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int n = JOptionPane.showConfirmDialog(
                this, "Want to delete solution?",
                "Delete Conformation",
                JOptionPane.YES_NO_OPTION);

        if (n == JOptionPane.YES_OPTION) {
            SQLutils sql = new SQLutils(this);
            int x = sql.delete(String.format("delete from \'%s\' where studID = \'%s\' and subID = \'%s\' and assNo = \'%s\'", Solution.table, StudentLoginPage.loggedInStudent.studentID, solutionAss));
            if (x != 1)
                Utils.showMessage(this, String.format("Error occurred during deletion, %d rows modified!", x));
        }
    }//GEN-LAST:event_deleteButtonActionPerformed


    private void uploadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser("C:\\%userprofile%\\Documents\\");
        int status = fileChooser.showSaveDialog(rootPane);
        String filePath;
        if (status == JFileChooser.APPROVE_OPTION)
        {
            filePath = fileChooser.getSelectedFile().getAbsolutePath();
        }
        else
            return;
        SQLutils sql = new SQLutils(this);
        sql.insert(new Solution(StudentLoginPage.loggedInStudent.studentID, SelectAssignmentPage.selectedSubject, solutionAss, filePath));
        sql.close();
    }//GEN-LAST:event_uploadButtonActionPerformed

